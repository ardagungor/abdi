export function rules(value) {
  switch (value) {
    case "2":
      return "Kendin iç";
    case "3":
      return "İstediğine içir";
    case "4":
      return "Herkes!";
    case "5":
      return "Erkekler";
    case "6":
      return "Kızlar";
    case "7":
      return "Ben hiç";
    case "8":
      return "Parmak masaya";
    case "9":
      return "Doğru/yanlış";
    case "10":
      return "Kural ekleme";
    case "JACK":
      return "Grup";
    case "QUEEN":
      return "Soruna cevap veriyorum :(";
    case "KING":
      return "Pas";
    case "ACE":
      return "Kelime ekleme";
  }
}
